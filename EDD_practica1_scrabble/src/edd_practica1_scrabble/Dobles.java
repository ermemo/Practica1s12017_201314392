package edd_practica1_scrabble;

/**
 *
 * @author guillermo
 */
public class Dobles {
   
    private int x;
    private int y;

    public Dobles() {
    }


    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    @Override
    public String toString() {
        return "Dobles{" + "x=" + x + ", y=" + y + '}';
    }


    
}
